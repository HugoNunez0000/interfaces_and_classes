# Definir un diccionario:
mydict = {
    'a': 1,
    'b': 2,
    'c': 3
}

# Recuperar todos los nombres:
print(mydict.keys())

# Recuperar todos los valores:
print(mydict.values())

# Recuperar nombres y valores:
print(mydict.items())

# Recuperar como lista de nombres:
keys = list(mydict.keys())
print(keys)